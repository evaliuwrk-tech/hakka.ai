# ---------- 組裝 ----------
def roc_label(key):
    y, m = int(key[:4]) - 1911, int(key[4:])
    return f"{y}年{m}月"

social = json.load(open(SOCIAL_JSON, encoding="utf-8"))
s_months = social["months"]
METRICS = ["posts", "likes", "reach", "shares", "comments", "views", "interactions"]

def platform_series(key):
    return {m: [social["monthly"][key][mo][m] for mo in s_months] for m in METRICS}

def weekly_fix(key):
    wk = social["weekly"][key]
    return {m: [wk[w][m] for w in social["weeks"]] for m in
            ["posts", "likes", "reach", "shares", "comments", "interactions"]}

